# soma entre variaveis 
a = 13
b = 20

valor = a + b # somando as variaveis  | ficaria 13+20, na matematica normal.

# metodo 1
print(f'A soma de {a} + {b} é {valor}')

# metodo 2
print('A soma de {} e {} é {}'.format(a, b, valor))
# com o metodo 2, as chaves que estão entre aspas, vão receber os valores do .format
# a chave 1 vai receber a, a chave 2 vai receber b, e a 3 chave vai receber  a var valor.


