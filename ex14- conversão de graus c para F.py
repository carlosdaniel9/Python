c = float(input('Digite a temperatura em ºC '))
f = ((9 * c) / 5) + 32 # essa é e formula de conversão de grau ºC para ºF
# o c da formula acima é a variavel, que esta entrando com um valor em graus ºC

print('a temperatura de {}ºC, convertida para ºF é {:.0f}'.format(c, f))



