num =  int(input('digite um número'))
num1 = num * 2      # pegando num e mutiplicando por 2
num2 = num * 3      # pegando num e mutiplicando por 3
num3 = num ** (1/2) # pegando o valor de num, e calculando sua raiz quadrada

print('O dobro de {} é {}'.format(num,num1))
print('O tripo de {} é {}'.format(num, num2))
print('a raiz quadrada de {} é {:.2f}'.format(num, num3))

